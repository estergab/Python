nota1 = float(input("Estou aqui para te ajudar a calcular sua média de notas! Vamos lá? \nDigite a primeira nota: \n"))
nota2 = float(input("Agora digite a segunda nota: \n"))
nota3 = float(input("Agora digite a terceira e última nota: \n"))

media = (nota1 + nota2 + nota3) / 3

print(f'Sua média é de {media:.2f}')